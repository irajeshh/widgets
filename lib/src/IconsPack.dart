  ///Private constructor
  const IconsPack._();

  ///Returns the names of material icons
  static List<String> materialIcons = _materialIconsMap.keys.toList();

  ///Returns the names of custom icons
  static List<String> customIcons = _customIconsMap.keys.toList();

  ///For ease of usage
  static IconData materialIcon(final String key) => icon(key);

  ///For ease of usage
  static IconData customIcon(final String key) {
    return icon(key, familyName: FontFamilyName.CustomIcons);
  }

  ///Returns the icon for the given code point value
  static IconData icon(
    final String key, {
    final FontFamilyName? familyName,
  }) {
    FontFamilyName? fN = familyName;
    int? codePoint;
    if (familyName != null) {
      codePoint = _iconsMap(familyName)[key];
    } else {
      codePoint = _customIconsMap[key];
      fN = FontFamilyName.CustomIcons;
      if (codePoint == null) {
        codePoint = _materialIconsMap[key];
        fN = FontFamilyName.MaterialIcons;
      }
    }

    if (codePoint != null && fN != null) {
      return IconData(codePoint, fontFamily: fN.name);
    } else {
      return Icons.help_outline;
    }
  }

  static Map<String, int> _iconsMap(final FontFamilyName name) {
    switch (name) {
      case FontFamilyName.CustomIcons:
        return _customIconsMap;
      default:
        return _materialIconsMap;
    }
  }

